    def convert_mf4_log(self):
        try:
            import asammdf
        except ImportError:
            raise ImportError("The 'asammdf' module is required. Please install it using 'pip install asammdf'.")
            
        mdf = asammdf.MDF(self.input_file)
        
        with SocketCANToAnyWriter(self.output_file, self.output_type) as logfile:
            try:
                start_epoch = mdf.header.start_time.timestamp()
            except Exception:
                start_epoch = 0.0

            try:
                occurrences = mdf.whereis("CAN_DataFrame.ID")
                if not occurrences:
                    raise Exception("No 'CAN_DataFrame.ID' found in MF4 file.")
                
                # whereis returns tuple of (group_idx, channel_idx)
                group_indices = set(grp for grp, ch in occurrences)

                all_frames = []
                for grp in group_indices:
                    try:
                        ids_sig = mdf.get("CAN_DataFrame.ID", group=grp)
                        datas_sig = mdf.get("CAN_DataFrame.DataBytes", group=grp)
                        buses_sig = mdf.get("CAN_DataFrame.BusChannel", group=grp)
                        ides_sig = mdf.get("CAN_DataFrame.IDE", group=grp)
                        
                        try:
                            dlcs_sig = mdf.get("CAN_DataFrame.DataLength", group=grp)
                        except Exception:
                            try:
                                dlcs_sig = mdf.get("CAN_DataFrame.DLC", group=grp)
                            except Exception:
                                dlcs_sig = None
                                
                        try:
                            rtrs_sig = mdf.get("CAN_DataFrame.RTR", group=grp)
                            rtrs = rtrs_sig.samples
                        except Exception:
                            rtrs = [0] * len(ids_sig.samples)

                        timestamps = ids_sig.timestamps
                        ids = ids_sig.samples
                        datas = datas_sig.samples
                        buses = buses_sig.samples
                        ides = ides_sig.samples
                        
                        if dlcs_sig is not None:
                            dlcs = dlcs_sig.samples
                        else:
                            dlcs = [8] * len(ids)

                        total_len = len(timestamps)
                        for i in range(total_len):
                            ts = timestamps[i] + start_epoch
                            bus = int(buses[i])
                            
                            can_id = int(ids[i])
                            ide = int(ides[i])
                            can_id_str = f"{can_id:03X}" if ide == 0 else f"{can_id:08X}"
                                
                            dlc = int(dlcs[i])
                            rtr = int(rtrs[i])
                            
                            data_arr = datas[i]
                            data_hex = "".join([f"{b:02X}" for b in data_arr[:dlc]])
                            
                            if rtr:
                                frame_part = f"{can_id_str}#R{dlc}"
                            else:
                                frame_part = f"{can_id_str}#{data_hex}"

                            all_frames.append((ts, bus, frame_part))
                    except Exception as e:
                        print(f"Skipping group {grp} due to error: {e}")
                        continue
                
                # Sort all frames chronologically
                all_frames.sort(key=lambda x: x[0])
                
                total_frames = len(all_frames)
                if total_frames == 0:
                    raise Exception("No valid CAN frames found across any groups.")
                    
                # Dynamically map bus IDs starting at 0 (CAN1->0, CAN2->1, etc.)
                unique_buses = sorted(list(set([f[1] for f in all_frames])))
                bus_map = {bus_raw: idx for idx, bus_raw in enumerate(unique_buses)}

                # Write to output file and give progress
                for i, (ts, bus_raw, frame_part) in enumerate(all_frames):
                    if i % 10000 == 0:
                        self.update_progress(int((i / total_frames) * os.path.getsize(self.input_file)))
                        
                    iface = f"can{bus_map[bus_raw]}"
                    logfile.write(f"({ts:.6f}) {iface} {frame_part}\n")
                    
            except Exception as e:
                raise Exception(f"Failed to extract raw CAN data from multiple groups. Error: {e}")

    def convert_css_txt(self):
        with open(self.input_file, "r") as readfile, SocketCANToAnyWriter(self.output_file, self.output_type) as logfile:
            base_year_month = ""
            file_size = os.path.getsize(self.input_file)
            current_bytes = 0
            
            for line_raw in readfile:
                current_bytes += len(line_raw)
                if current_bytes % 32768 < 200:
                    self.update_progress(current_bytes)
                
                line_str = line_raw.strip()
                if line_str.startswith("# Time:"):
                    parts = line_str.split(" ")
                    if len(parts) > 2:
                        base_year_month = parts[2][:6]
                    else:
                        base_year_month = "202301"
                    continue
                if not line_str or line_str.startswith('#') or line_str.startswith('Timestamp'):
                    continue
                
                parts = line_str.split(';')
                if len(parts) < 4: continue
                
                try:
                    ts_str = parts[0]
                    from datetime import datetime
                    if base_year_month == "":
                        base_year_month = "202301"
                    dt_str = base_year_month + ts_str.replace("T", "")
                    
                    if len(dt_str) >= 14:
                        ms = float(dt_str[14:]) / 1000.0 if len(dt_str) > 14 else 0.0
                        dt = datetime.strptime(dt_str[:14], "%Y%m%d%H%M%S")
                        timestamp = dt.timestamp() + ms
                    else:
                        timestamp = 0.0
                    
                    bus_channel = int(parts[1]) - 1 if parts[1] else 0
                    can_id = int(parts[2], 16)
                    data_str = parts[3]
                    
                    id_fmt = f"{can_id:08X}" if can_id > 0x7FF else f"{can_id:03X}"
                    can_if = f"can{bus_channel}"
                    
                    logfile.write(f"({timestamp:.6f}) {can_if} {id_fmt}#{data_str.upper()}\n")
                except Exception:
                    pass
            self.update_progress(file_size)
